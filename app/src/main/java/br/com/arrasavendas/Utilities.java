    public static String makePlaceholders(int len) {
        if (len < 1) {
            // It will lead to an invalid query anyway ..
            throw new RuntimeException("No placeholders");
        } else {
            StringBuilder sb = new StringBuilder(len * 2 - 1);
            sb.append("?");
            for (int i = 1; i < len; i++) {
                sb.append(",?");
            }
            return sb.toString();
        }
    }

    public static Drawable convertDrawableToGrayScale(Drawable drawable) {
        if (drawable == null) {
            return null;
        }
        Drawable res = drawable.mutate();
        res.setColorFilter(Color.GRAY, PorterDuff.Mode.SRC_IN);
        return res;
    }

    public static Integer[] interval(int start, int end) {
        if (start > end)
            throw new IllegalArgumentException(String.format("intervalo inv√°lido: [%d,%d]", start, end));
        Integer[] array = new Integer[end - start + 1];
        int length = array.length;

        for (int i = 0; i < length; ++i) {
            array[i] = i + start;
        }

        return array;
    }

    /**
     * Salva uma imagem em alguma das pastas de imagens do arrasa vendas
     *
     * @param ctx
     * @param folder    determina a pasta
     * @param imageName
     * @param uri
     * @return path completo para o arquivo na pasta local
     */
    public static String salvarImagem(Context ctx, ImageFolder folder, String imageName, Uri uri) throws IOException {
        File file = new File(folder.getPath(ctx) + File.separator + imageName);
        ContentResolver contentResolver = ctx.getContentResolver();

        InputStream inputStream = contentResolver.openInputStream(uri);
        ByteArrayOutputStream outputStream = new ByteArrayOutputStream();
        byte[] buffer = new byte[4 * 1024];
        int read = 0;

        while ((read = inputStream.read(buffer)) != -1) {
            outputStream.write(buffer, 0, read);
        }

        FileOutputStream output = new FileOutputStream(file);
        output.write(outputStream.toByteArray());
        output.close();
        inputStream.close();

        return file.getAbsolutePath();

    }

    /**
     * http://developer.android.com/intl/pt-br/training/displaying-bitmaps/load-bitmap.html#load-bitmap
     *
     * @param options
     * @param reqWidth
     * @param reqHeight
     * @return
     */
    public static int calculateInSampleSize(BitmapFactory.Options options, int reqWidth, int reqHeight) {
        // Raw height and width of image
        final int height = options.outHeight;
        final int width = options.outWidth;
        int inSampleSize = 1;

        if (height > reqHeight || width > reqWidth) {

            final int halfHeight = height / 2;
            final int halfWidth = width / 2;

            // Calculate the largest inSampleSize value that is a power of 2 and keeps both
            // height and width larger than the requested height and width.
            while ((halfHeight / inSampleSize) > reqHeight
                    && (halfWidth / inSampleSize) > reqWidth) {
                inSampleSize *= 2;
            }
        }

        return inSampleSize;
    }

    /**
     * @return The MIME type for the given file.
     */
    public static String getMimeType(File file) {

        String extension = getExtension(file.getName());

        if (extension.length() > 0)
            return MimeTypeMap.getSingleton().getMimeTypeFromExtension(extension.substring(1));

        return "application/octet-stream";
    }

    public static String getMimeType(Context context, Uri uri) {
        File file = new File(getPath(context, uri));
        return getMimeType(file);
    }

    /**
     * Gets the extension of a file name, like ".png" or ".jpg".
     *
     * @param uri
     * @return Extension including the dot("."); "" if there is no extension;
     * null if uri was null.
     */
    public static String getExtension(String uri) {
        if (uri == null) {
            return null;
        }

        int dot = uri.lastIndexOf(".");
        if (dot >= 0) {
            return uri.substring(dot);
        } else {
            // No extension.
            return "";
        }
    }

